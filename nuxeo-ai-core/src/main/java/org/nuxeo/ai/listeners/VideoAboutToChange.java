package org.nuxeo.ai.listeners;

import org.nuxeo.ecm.core.api.DocumentModel;
import org.nuxeo.ecm.core.event.Event;
import org.nuxeo.ecm.core.event.EventContext;
import org.nuxeo.ecm.core.event.EventListener;
import org.nuxeo.ecm.core.event.impl.DocumentEventContext;

public class VideoAboutToChange implements EventListener {

    public static final String CAPTIONABLE_FACET = "Captionable";

    public static final String VIDEO_FACET = "Video";

    @Override
    public void handleEvent(Event event) {
        EventContext ctx = event.getContext();
        if (!(ctx instanceof DocumentEventContext)) {
            return;
        }
        DocumentEventContext docCtx = (DocumentEventContext) ctx;
        DocumentModel doc = docCtx.getSourceDocument();
        if (doc.hasFacet(VIDEO_FACET)
                && !doc.hasFacet(CAPTIONABLE_FACET)
                && !doc.isProxy()) {
            doc.addFacet(CAPTIONABLE_FACET);
        }
    }
}
